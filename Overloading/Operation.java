package Overloading;

public class Operation {
    //Method 1 : add two integers
    public int add(int a, int b) {
        return a + b;
    }

    //Method 2 : add three integers
    public int add(int a, int b, int c) {
        return a + b + c;
    }

    //Method 3 : add two doubles
    public double add(double a, double b) {
        return a + b;
    }
}
